public class MethodPractice01 {
	public static void main (String[] args) {
	
		
		int f = 4;
		int s = 6;
		multiplier(f, s);
		int multi = multiplier(f, s);
		System.out.println(multi);
		
		
		
		
		
		
		
	}
	public static int multiplier (int t, int p) {
		int m = t * p;
		return m;
		
		
		
	}
	
}