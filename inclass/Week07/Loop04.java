public class Loop04 {
	public static void main (String[] args) { 
		
		
		for (int i = 0 ; i <= 12 ; i++ ) {
			for (int j = 0 ; j <= 12 ; j++ ) {
				System.out.println(i + " X " + j + " = " + (i * j) );
			}
		// loop will run 10 (for i) * 12 (for j) times. 
		// double loops are mainly used for multiplication problems. 
			
		}
		// double loop: is a loop inside a loop. 
		// for double loop, usually need two indexes
	}
}
